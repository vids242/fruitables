import { GET_CART } from "../ActionType"

const initialState = {
    isLoding: false,
    cart: [],
    error: null
}

export const cartReducer = (state = initialState, action) => {

    switch (action.type) {
        case GET_CART:

            return {
                isLoding: false,
                cart: action.payload,
                error: null
            }
            default:
                return state;
        }
       
}